import { dirname, join } from 'path';
import { mkdir, stat, writeFile } from 'fs/promises';
import { resolveProjectById, getProjectById } from './projects.js';
import { buildTree } from './requirements.js';
import type { TreeNode } from '../types/project.js';

interface Branding {
  primary: string;
  secondary: string;
}

interface ScaffoldResult {
  success: boolean;
  outputPath: string;
  filesCreated: number;
  logs: string[];
}

/* ------------------------------------------------------------------ */
/*  Helper: convert requirement title to URL-safe slug                 */
/* ------------------------------------------------------------------ */

function slugify(name: string): string {
  return name
    .replace(/^[A-Z]+-[\d.]+[:\s]*/i, '') // strip prefix like "SOL-1: "
    .trim()
    .toLowerCase()
    .replace(/[äÄ]/g, 'ae')
    .replace(/[öÖ]/g, 'oe')
    .replace(/[üÜ]/g, 'ue')
    .replace(/ß/g, 'ss')
    .replace(/[^a-z0-9]+/g, '-')
    .replace(/^-+|-+$/g, '')
    || 'unnamed';
}

/* ------------------------------------------------------------------ */
/*  Helper: safe string for JSX/TS templates                           */
/* ------------------------------------------------------------------ */

function safe(str: string, maxLen = 80): string {
  return str.replace(/'/g, "\\'").replace(/`/g, "'").slice(0, maxLen);
}

/* ------------------------------------------------------------------ */
/*  Helper: PascalCase identifier from a slug                          */
/* ------------------------------------------------------------------ */

function pascal(slug: string): string {
  return slug
    .split('-')
    .map((s) => s.charAt(0).toUpperCase() + s.slice(1))
    .join('');
}

