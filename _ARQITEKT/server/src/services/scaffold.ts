function genRootLayout(projectName: string, sols: TreeNode[]): string {
  const navLinks = sols
    .map((sol) => {
      const slug = slugify(sol.title);
      const label = safe(sol.title, 30);
      return `            <a href="/${slug}">${sol.id}: ${label}</a>`;
    })
    .join('\n');

  return `// @generated by ARQITEKT Scaffold
import type { Metadata } from 'next';
import './globals.css';

export const metadata: Metadata = {
  title: '${safe(projectName)}',
  description: 'Generated by ARQITEKT Product Factory',
};

export default function RootLayout({ children }: { children: React.ReactNode }) {
  return (
    <html lang="de">
      <body>
        <header style={{ background: 'var(--color-bg2)', borderBottom: '1px solid var(--color-border)', padding: '0 var(--space-6)', height: 56, display: 'flex', alignItems: 'center', gap: 'var(--space-6)' }}>
          <h1 style={{ fontSize: 18, fontWeight: 700, color: 'var(--color-brand-gold)', whiteSpace: 'nowrap' }}>
            ${safe(projectName)}
          </h1>
          <nav style={{ display: 'flex', gap: 'var(--space-4)', overflowX: 'auto' }}>
${navLinks}
          </nav>
        </header>
        <main style={{ maxWidth: 1200, margin: '0 auto', padding: 'var(--space-6)' }}>
          {children}
        </main>
      </body>
    </html>
  );
}
`;
}

